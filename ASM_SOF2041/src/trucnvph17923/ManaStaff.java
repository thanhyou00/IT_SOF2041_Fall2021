    private  NhanVienDAO dao = new NhanVienDAO();
    int row = -1;
    private void init() {
        this.fillTable();
        this.row = -1;
        this.updateStatus();
    }
    private void insert() {
        Staff nv = getForm();
        String mk2 = new String(txtmk2.getPassword());
        if(!mk2.equals(nv.getMatkhau())) {
            MsgBox.alert(this, "Xác nhận mật khẩu không đúng");
            return;
        } else {
            try {
                dao.insert(nv);
                this.fillTable();
                this.clear();
                MsgBox.alert(this, "Thêm mới thành công !");
                return;
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm mới thất bại !");
                return;
            }
        }
    }

    private void update() {
        Staff nv = getForm();
        String mk2 = new String(txtmk2.getPassword());
        if(!mk2.equals(nv.getMatkhau())){
            MsgBox.alert(this, "Xác nhận mật khẩu không đúng !");
        } else {
            try {
                dao.update(nv);
                this.fillTable();
                MsgBox.alert(this, "Cập nhật thành công !");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại !");
            }
        }
    }

    private void delete() {
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa nhân viên !");
        } else {
            String manv = txtmanv.getText();
            if(manv.equals(Auth.user.getManv())) {
                MsgBox.alert(this, "Bạn không được xóa chính bạn !");
            } else if(MsgBox.confirm(this, "Bạn thực sự muốn xóa nhân viên này ?")){
                try {
                    dao.delete(manv);
                    this.fillTable();
                    this.clear();
                    MsgBox.alert(this, "Xóa thành công !");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại !");
                }
            }
        }
    }

    private void edit() {
        String manv = (String) tblnhanvien.getValueAt(row, 0);
        Staff nv = dao.selectbyId(manv);
        this.setForm(nv);
        tabs.setSelectedIndex(0);
        this.updateStatus();
    }

    private void first() {
        this.row=0;
        this.edit();
    }

    private void prev() {
        if(this.row>0) {
            this.row--;
            this.edit();
        }
    }

    private void next() {
        if(this.row<tblnhanvien.getRowCount()-1){
            this.row++;
            this.edit();
        }
    }

    private void last() {
        this.row= tblnhanvien.getRowCount()-1;
        this.edit();
    }

    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblnhanvien.getModel();
        model.setRowCount(0);
        try {
           List<Staff> list = dao.selectAll();
            for (Staff nv : list) {
                Object[] row = {
                nv.getManv(),nv.getMatkhau(),nv.getHoten(),nv.getVaitro()?"Trưởng phòng":"Nhân viên"
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu !");
        }
    }

    private void setForm(Staff nv) {
        txtmanv.setText(nv.getManv());
        txthoten.setText(nv.getHoten());
        txtmk.setText(nv.getMatkhau());
        txtmk2.setText(nv.getMatkhau());
        rdotruongphong.setSelected(nv.getVaitro());
        rdonhanvien.setSelected(!nv.getVaitro());
    }

    private Staff getForm() {
        Staff nv = new Staff();
        nv.setManv(txtmanv.getText());
        nv.setHoten(txthoten.getText());
        nv.setMatkhau(new String(txtmk.getPassword()));
        nv.setVaitro(rdotruongphong.isSelected());
        return nv;
    }

    private void updateStatus() {
        boolean edit = this.row>=0;
        boolean first = this.row==0;
        boolean last = this.row==tblnhanvien.getRowCount()-1;
        // Trạng thái form
        txtmanv.setEditable(!edit);
        btnthem.setEnabled(!edit);
        btnsua.setEnabled(edit);
        btnxoa.setEnabled(edit);
        // Trạng thái điều hướng
        btnfirst.setEnabled(edit&&!first);
        btnpre.setEnabled(edit&&!first);
        btnnext.setEnabled(edit&&!last);
        btnlast.setEnabled(edit&&!last);
    }
    
    private void clear(){
        Staff nv = new Staff();
        this.setForm(nv);
        this.row=-1;
        this.updateStatus();
    }




